// Function to get a placeholder image based on food name and type
export const getFoodImage = (foodName, foodType = null) => {
  // First try to find an exact match in the categorized images
  for (const category in foodPlaceholderImages) {
    if (foodPlaceholderImages[category][foodName]) {
      return foodPlaceholderImages[category][foodName];
    }
  }

  // If no exact match, try to match by type
  if (foodType && genericFoodImages[foodType.toLowerCase()]) {
    return genericFoodImages[foodType.toLowerCase()];
  }

  // Return default image if no match found
  return genericFoodImages.default;
};

